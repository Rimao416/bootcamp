const Tour = require("./../models/tourModel");

exports.getTour = async (req, res) => {
  try {
    // First Way
    const tour = await Tour.findById(req.params.id);
    // Tour.findOne({_id:req.params.id}) Second Way
    res.status(200).json({
      status: "success",
      data: {
        tour,
      },
    });
  } catch (error) {
    res.status(404).json({
      status: "fail",
      message: error,
    });
  }
};
